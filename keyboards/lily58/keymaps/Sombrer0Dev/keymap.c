bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode){
    case LNG_SWC:
      if (record->event.pressed) {
        register_code(KC_LGUI);
        register_code(KC_SPC);
      } else {
        unregister_code(KC_LGUI);
        unregister_code(KC_SPC);

      }
  }
  if (record->event.pressed) {
#ifdef OLED_ENABLE
    set_keylog(keycode, record);
#endif
    // set_timelog();
  }
  return true;
}
